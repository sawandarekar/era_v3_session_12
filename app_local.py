def generate_text(
    model,
    prompt,
    max_length,
    num_return_sequences,
    device
): 
    # tokenizer = tokenizer = AutoTokenizer.from_pretrained('gpt2')
    input_ids = tokenizer.encode(prompt)
    input_ids = torch.tensor(input_ids, dtype=torch.long, device=device)
    input_ids = input_ids.unsqueeze(0).repeat(num_return_sequences, 1)
    input_ids = input_ids.to(device)

    #Calculate final length
    final_length = input_ids.shape[1] + max_length

    #Generate text
    with torch.no_grad():
        while input_ids.shape[1] < final_length:
            logits = model(input_ids)[0]
            next_token_logits = logits[:, -1, :]
            probs = torch.softmax(next_token_logits, dim=-1)
            next_token = torch.multinomial(probs, num_samples=1)
            input_ids = torch.cat([input_ids, next_token], dim=-1)  
    

    generated_text = []
    for i in range(num_return_sequences):
        tokens = input_ids[i].tolist()
        text = tokenizer.decode(tokens)
        generated_text.append(text)

    return generated_text

def generate(prompt, num_sequences, max_length):
    if not prompt:
        return "Please enter a prompt."
    
    generated_texts = generate_text(
        model=model,
        prompt=prompt,
        max_length=max_length,
        num_return_sequences=num_sequences,
        device=device
    )
    
    # Format output with sequence numbers
    formatted_output = ""
    for i, text in enumerate(generated_texts, 1):
        formatted_output += f"**Sequence {i}**:\n{text}\n\n"
    
    return formatted_output
